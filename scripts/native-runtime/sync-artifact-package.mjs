import { copyFile, cp, mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import process from 'node:process'
import { pathToFileURL } from 'node:url'

import {
  assertNativeRuntimeAssets,
  resolveNativeRuntimeAssets,
  resolveWorkspaceRoot,
} from './assets.mjs'

const artifactPackageDir = 'packages/luma-native-artifacts'

const nativeFileCopies = [
  [
    'packages/luma-raw-runtime/dist/native/desktop/luma_raw.js',
    `${artifactPackageDir}/native/desktop/luma_raw.js`,
  ],
  [
    'packages/luma-raw-runtime/dist/native/desktop/luma_raw.wasm',
    `${artifactPackageDir}/native/desktop/luma_raw.wasm`,
  ],
  [
    'packages/luma-raw-runtime/dist/native/low-memory/luma_raw.js',
    `${artifactPackageDir}/native/low-memory/luma_raw.js`,
  ],
  [
    'packages/luma-raw-runtime/dist/native/low-memory/luma_raw.wasm',
    `${artifactPackageDir}/native/low-memory/luma_raw.wasm`,
  ],
  [
    'packages/luma-jpeg-runtime/dist/native/luma_jpeg.js',
    `${artifactPackageDir}/native/luma_jpeg.js`,
  ],
  [
    'packages/luma-jpeg-runtime/dist/native/luma_jpeg.wasm',
    `${artifactPackageDir}/native/luma_jpeg.wasm`,
  ],
  [
    'packages/luma-raw-runtime/dist/native/desktop/provenance.json',
    `${artifactPackageDir}/native/provenance/raw-desktop.json`,
  ],
  [
    'packages/luma-raw-runtime/dist/native/low-memory/provenance.json',
    `${artifactPackageDir}/native/provenance/raw-low-memory.json`,
  ],
  [
    'packages/luma-jpeg-runtime/dist/native/provenance.json',
    `${artifactPackageDir}/native/provenance/jpeg.json`,
  ],
]

async function copyRequiredFile(rootDir, from, to) {
  const sourcePath = join(rootDir, from)
  const targetPath = join(rootDir, to)
  await mkdir(dirname(targetPath), { recursive: true })
  await copyFile(sourcePath, targetPath)
}

async function writeCombinedThirdPartyNotices(rootDir) {
  const [rawNotices, jpegNotices] = await Promise.all([
    readFile(
      join(rootDir, 'packages/luma-raw-runtime/THIRD_PARTY_NOTICES.md'),
      'utf8',
    ),
    readFile(
      join(rootDir, 'packages/luma-jpeg-runtime/THIRD_PARTY_NOTICES.md'),
      'utf8',
    ),
  ])

  await writeFile(
    join(rootDir, artifactPackageDir, 'THIRD_PARTY_NOTICES.md'),
    [
      '# Third-Party Notices',
      '',
      'This package redistributes prebuilt WebAssembly artifacts generated by the LumaForge RAW and JPEG runtime packages.',
      '',
      '## RAW Runtime',
      '',
      rawNotices,
      '',
      '## JPEG Runtime',
      '',
      jpegNotices,
      '',
    ].join('\n'),
  )
}

async function copyComplianceFiles(rootDir) {
  await copyRequiredFile(rootDir, 'LICENSE', `${artifactPackageDir}/LICENSE`)
  await rm(join(rootDir, artifactPackageDir, 'THIRD_PARTY_LICENSES'), {
    force: true,
    recursive: true,
  })
  await mkdir(join(rootDir, artifactPackageDir, 'THIRD_PARTY_LICENSES'), {
    recursive: true,
  })
  await Promise.all([
    cp(
      join(rootDir, 'packages/luma-raw-runtime/THIRD_PARTY_LICENSES'),
      join(rootDir, artifactPackageDir, 'THIRD_PARTY_LICENSES'),
      { recursive: true },
    ),
    cp(
      join(rootDir, 'packages/luma-jpeg-runtime/THIRD_PARTY_LICENSES'),
      join(rootDir, artifactPackageDir, 'THIRD_PARTY_LICENSES'),
      { recursive: true },
    ),
    writeCombinedThirdPartyNotices(rootDir),
  ])
}

export async function syncNativeArtifactPackage({
  rootDir = process.cwd(),
} = {}) {
  rootDir = resolveWorkspaceRoot(rootDir)
  const sourceAssetSets = resolveNativeRuntimeAssets({
    rootDir,
    mode: 'source',
  })
  assertNativeRuntimeAssets(sourceAssetSets)

  await rm(join(rootDir, artifactPackageDir, 'native'), {
    force: true,
    recursive: true,
  })
  await Promise.all(
    nativeFileCopies.map(([from, to]) => copyRequiredFile(rootDir, from, to)),
  )
  await copyComplianceFiles(rootDir)
}

if (
  process.argv[1] &&
  import.meta.url === pathToFileURL(process.argv[1]).href
) {
  syncNativeArtifactPackage().catch((error) => {
    console.error(error)
    process.exitCode = 1
  })
}
